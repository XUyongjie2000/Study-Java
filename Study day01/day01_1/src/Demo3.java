public class Demo3 {
    //所有java程序，如果需要执行，必须有main方法，main方法是一个程序的入口
    public static void main(String[] args) {
        //sout: System.out.println 执行完成，会自动换行
        System.out.println("我用来输出控制台文字");
        System.out.println("1");

        // System.out.print 执行完成，无换行
        System.out.print("3");
        System.out.print("4");
    }
    
}
