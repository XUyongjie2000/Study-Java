package com.czxy.demo1;

public class Demo3 {
    public static void main(String[] args) {
        //关系运算符
        System.out.println(1==1);//true
        System.out.println(1==2);//false
        System.out.println(1!=1);//false
        System.out.println(1!=2);//true
        System.out.println("---");
        System.out.println(1<1);//false
        System.out.println(1<2);//true

        System.out.println(1>1);//false
        System.out.println(2>1);//true
        System.out.println("---");
        System.out.println(1<=1);//true
        System.out.println(1<=2);//true

        System.out.println(1>=1);//true
        System.out.println(2>=1);//true
    }
}
