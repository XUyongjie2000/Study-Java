package com.czxy.demo2;

public class Demo3 {
    public static void main(String[] args) {
        //1、定义变量，赋值10
        int num=10;
        //2、+9，保存结果，展示控制台
        num = num+9;
        System.out.println(num);
        //3、-1，保存结果，展示控制台
        num = num-1;
        System.out.println(num);
        //4、*2，保存结果，展示控制台
        num = num*2;
        System.out.println(num);
        //5、/9，保存结果，展示控制台
        num = num/9;
        System.out.println(num);
    }
}
